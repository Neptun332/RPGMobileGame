package com.example.patry.rpgmobilegame.player;

public class Character {

    public Character(String name, int strength, int agility, int vitality) {
        this.name = name;
        this.strength = strength;
        this.agility = agility;
        this.vitality = vitality;
    }

    public String name;
    public int strength;
    public int agility;
    public int vitality;

}
